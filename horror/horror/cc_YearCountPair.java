public class cc_YearCountPair{
  float count;
  int year_value;
  int budget;
  
  public cc_YearCountPair(float count,int year_value){
    this.count=count;
    this.year_value=year_value;
  }
  
  public float getCount(){
    return count; 
  }
  
  public int getYear(){
    return year_value;
  }
  
  public int getBudget(){
    return budget;
  }
}
